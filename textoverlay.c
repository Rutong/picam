// WARNING: this function is not performing any boundary check
static void put_char(char asc, uint8_t* ptr, int stride) {
  int idx,c,r;
  uint8_t* rp;
  uint8_t* tgt;

  if(asc<32 || asc>126)
    return; // no printable

  idx = asc-32;
  tgt = ptr;

  for(r=0;r<FONT_CHAR_HEIGHT;r++){
    tgt = ptr + r * stride;
    rp = font + idx * FONT_CHAR_WIDTH + r * FONT_STRIDE;
    for(c=0;c<FONT_CHAR_WIDTH;c++){
      if(*rp>15) *tgt=*rp;
      tgt++;
      rp++;
    }
  }
}

// WARNING: this function is not performing any boundary check
static void put_string(char* string, uint8_t* ptr, int stride) {
  char* cp;
  uint8_t* tgt;

  cp=string;
  tgt=ptr;
  
  while(*cp!='\0'){
    put_char(*cp, tgt, stride);
    tgt+=FONT_PRINT_WIDTH;
    cp++;
  }
}

// WARNING: this function is not performing any boundary check
static void draw_string_at_position(char* string, uint8_t* buf, int x, int y, int stride) {
  put_string(string, buf + y*stride+x, stride);
}

void add_timestamp_overlay(int width, int height, uint8_t *data) {
  const int width32 = ((width + 31) & ~31); // nearest smaller number that is multiple of 32
  //const int height16 = ((height + 15) & ~15); // nearest smaller number that is multiple of 16
  char time_base[20];
  time_t rawtime;
  struct tm *timeinfo;

  time(&rawtime);
  timeinfo = localtime(&rawtime);
  strftime(time_base, 20, "%Y-%m-%d %H:%M:%S", timeinfo);

  draw_string_at_position(time_base, data, width-TIMESTAMP_WIDTH-TIMESTAMP_MARGIN, height-TIMESTAMP_HEIGHT-TIMESTAMP_MARGIN, width32);
}

